import { random } from "./random_util";

export class Sprite {
  constructor(string, x, y, spaces) {
    this.string = string;
    this.baseX = x;
    this.baseY = y;
    this.displayX = x;
    this.displayY = y;
    this.phase = random(0, Math.PI * 2);
    this.offset = Math.floor(random(1, 4));
    this.spaces = spaces;
    this.getDimensions();
  }

  getDimensions() {
    const lines = this.string.split("\n");
    lines.sort((a, b) => b.length - a.length);
    this.width = lines[0].length;
    this.height = lines.length;
  }

  bob() {
    this.phase += 0.01;
    const yOff = Math.sin(this.phase) * this.offset;
    this.displayY = this.baseY + Math.floor(yOff);
  }
}

